package com.example.DongAisa.dto;

public class FormData {
    private String id;
    private String password;

    public FormData() {
        // 기본 생성자
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
